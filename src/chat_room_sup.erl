-module(chat_room_sup).

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).

-define(SERVER, ?MODULE).

%%%===================================================================
%%% API functions
%%%===================================================================

start_link() ->
	supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%%===================================================================
%%% Supervisor callbacks
%%%===================================================================

init([]) ->
	MainRoom = {<<"main">>,
		{chat_room, start_link, [<<"main">>]},
		permanent, 3000, worker, [chat_room]
	},
	{ok, { {one_for_one, 1, 30}, [MainRoom]} }.

