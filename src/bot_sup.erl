-module(bot_sup).

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).

-define(SERVER, ?MODULE).

%%%===================================================================
%%% API functions
%%%===================================================================

start_link() ->
	supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%%===================================================================
%%% Supervisor callbacks
%%%===================================================================

init([]) ->
	BotExample = {bot_example, {bot_example, start_link, []},
		permanent, 2000, worker, [bot_example]},

	{ok, {{one_for_one, 1, 30}, [BotExample]}}.

